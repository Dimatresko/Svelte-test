<h1>Как мы тренируемся, чтобы стать профессиональными инженерами</h1>

<p>Стать инженером - это одновременно весело и непросто.</p>

<p>Для начала сузьте круг своих интересов до интересующего вас инженерного направления и поступайте в университет, который предлагает эффективную программу в области ит. Приобретите необходимый опыт работы с помощью стажировок и других программ работы и обучения, а также общайтесь с другими инженерами. Со временем и самоотдачей вы скоро станете инженером.</p> 
